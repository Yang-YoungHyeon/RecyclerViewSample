package com.sample.recyclerview;

/** RecyclerView ListItem */
public class RecyclerviewItem {
    /** mTime : Used Save Time */
    private String mTime;

    /** Constructor */
    public RecyclerviewItem(String mTime) {
        this.mTime = mTime;
    }

    /** getmTime() : return Data */
    public String getmTime() {
        return mTime;
    }
}
